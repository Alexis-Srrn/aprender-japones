          <h1 id="sub-1">Declarando que algo es así usando「だ」</h1>
          <p>Uno de los aspectos más difíciles del japonés es que no hay ningún verbo para declarar
            el estado-de-ser como, en español, el verbo “ser”. Lo que tiene el japonés en su lugar es una
            forma de declarar que algo es como es añadiendo el carácter hiragana 「だ」
            a un nombre o adjetivo-na solo. Ya verás lo que significa esto cuando abordemos
            el estudio de los nombres y de los adjetivos.
        </p>

        <p class="leccion">Declarando que algo es así usando 「だ」</p>
        <p>（１） <abbr title="さかな - pez">魚</abbr>  - Pez.</p>
        <p>  （２）<abbr title="さかな - pez">魚</abbr><b class="naranja"> だ。</b> - Es un pez. </p>
        <p></p>

        <p>Parece muy fácil. Pero ahora viene la parte mala.</p>

        <p class="leccion">¡Un estado-de-ser puede ser implícito! 「だ」</p>
        <p>Tal cual, （１） es nada más que la palabra "pez", no significa nada aparte de eso. Pero ya veremos en
          la próxima sección que, con la partícula de tema, podemos inferir a partir del contexto que algo es un pez
          sin necesidad de que declaremos nada.
          Y la pregunta que debe de estar rondándote en la cabeza es ¿Y si se puede decir que algo es [X] sin utilizar
          「だ」 ¿de que sirve entonces que aparezca? Pues bien, la diferencia principal es que una afirmación declarada
           hace que la frase suene más enfática y enérgica con el fin de hacerla más... eso, declarativa.
            Por lo tanto, es más normal que sean los hombres los que utilicen 「だ」 al final de una frase.
            Esta es la razón por la que no se puede utilizar cuando se está haciendo una pregunta,
            porque sonaría como si estuviésemos haciendo una afirmación y una pregunta al mismo tiempo. </p>



        <h1 id="sub-2">Construyendo la forma negativa del estado-de-ser.</h1>

        <p>En japonés, el negativo y el pasado se forman conjugando. Podemos conjugar el estado-de-ser a negativo o a pasado para decir que algo no es [X] o que algo era [X]. Esto va a ser un poco difícil de pillar
           al principio, pero ninguna de estas conjugaciones del estado-de-ser cumple la función declarativa que tiene 「だ」. </p>

        <p>Primero, para formar el pasado negativo solo hay que añadir 「じゃない」 al nombre o adjetivo-na.</p>

        <p class="leccion">Reglas de conjugación para el estado-de-ser negativo</p>
        <p>Unir 「じゃない」 al nombre o adjetivo-na</p>
        <p>（例） <abbr title="ともだち - amigo">友達</abbr>　→　<abbr title="ともだち - amigo">友達</abbr>じゃない (no es amigo)</p>

        <p>Ejemplos:</p>
        <p>（１）　<abbr title="さかな - pez">魚</abbr><b class="naranja">じゃない。</b>- No es pez.</p>
        <p>（２）　<abbr title="がくせい - estudiante">学生</abbr><b class="naranja">じゃない。</b>- No es estudiante.</p>
        <p>（３）　<abbr title="しずか - tranquilo">静</abbr>か<b class="naranja">じゃない。</b>- No es tranquilo. </p>


        <h1 id="sub-3">Construyendo el pasado del estado-de-ser</h1>

        <p>Ahora vamos a ver el pasado del estado-de-ser. Para decir que algo era o fue algo se añade「だった」 al nombre o adjetivo-na. La forma negativa se puede transformar en pasado negativa sustituyendo la 「い」 de 「じゃない」 por 「かった」. </p>

        <p class="leccion">Reglas de conjugación para el pasado del estado-de-ser.</p>
        <p></p>

        <ol>
          <li><p><b>Pasado del estado-de-ser:</b> Se añade「だった」al nombre</p></li>
          <p>（例） <abbr title="ともだち - amigo">友達</abbr>　→　<abbr title="ともだち - amigo">友達</abbr>だった (era amigo)</p>
          <li><p><b>Pasado Negativo del estado-de-ser:</b>Se conjuga primero el nombre o adjetivo-na al pasado negativo y después se sustituye la 「い」 de 「じゃない」 por 「かった</p></li>
          <p>（例） <abbr title="ともだち - amigo">友達</abbr>　→　<abbr title="ともだち - amigo">友達</abbr>じゃない →　友達じゃなかった (no era amigo) </p>
        </ol>



        <p>（１）　<abbr title="さかな - pez">魚</abbr><b class="naranja">だった。</b>- Era un pez.</p>
        <p>（２）　<abbr title="がくせい - estudiante">学生</abbr>じゃな<b class="naranja">かった。</b>- No era estudiante.</p>
        <p>（３）　<abbr title="しずか - tranquilo">静</abbr>かじゃな<b class="naranja">かった。</b>- No era tranquilo. </p>

