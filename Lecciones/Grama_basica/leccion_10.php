          <h1 id="sub-1">Las tres ultimas partículas (¡No!)</h1>
                            <p>Ya hemos visto las principales construcciones gramaticales, conociéndolas podemos expresar casi todo lo que queramos. Ahora vamos a ocuparnos de la partícula 「の」, con ella vamos a poder expresarnos aún mejor, ya que nos va a permitir definir un nombre genérico abstracto. También vamos a aprender a modificar directamente a los nombres mediante nombres. Las tres partículas de las que nos vamos a ocupar a continuación sirven para agrupar nombres de diversas formas. </p>
                            <p>Ésta es la última lección que va a tratar de forma específica sobre las partículas, pero esto no quiere decir que no queden partículas por aprender. Aprenderemos muchas más a lo largo del camino, pero puede que, cuando las encontremos, no aparezcan etiquetadas como tales. Mientras sepas lo que significan y cómo utilizarlas no es demasiado importante que sepas que se trata de partículas.</p>


          <h1 id="sub-2">La partícula Inclusiva 「と」</h1>

                              <p>La partícula 「と」 es similar a la partícula 「も」 en que presenta un matiz de inclusión. Puede servir para unir dos o más nombres, teniendo entonces el significado de “y”. </p>
                              <p>（１）　<abbr title="スプーン - cuchara">スプーン</abbr><b class="naranja">と</b><abbr title="フォーク - tenedor">フォーク</abbr>で<abbr title="さかな - pescado">魚</abbr>を<abbr title="たべる">食べた</abbr>。</p>
                              <p>  - Haber comido pescado por medio de cuchara y tenedor.</p>
                              <p>（２）　<abbr title="ほん - libro">本</abbr><b class="naranja">と</b><abbr title="さっし - revista">雑誌</abbr><b class="naranja">と</b><abbr title="はがき - postal">葉書</abbr>を<abbr title="かう - comprar">買った</abbr>。</p>
                              <p>  - Comprado libro, revista, y postal.</p><br>

                              <p>Otro uso similar de la partícula 「と」es mostrar una acción que se llevó a cabo con alguien más o con algo. </p>
                              <p>（１）　<abbr title="ともだち - amigo">友達</abbr><b class="naranja">と</b><abbr title="はなす - hablar">話した。</abbr></p>
                              <p>- Hablé con amigo.</p>
                              <p>（２）　<abbr title="せんせい - profesor">先生</abbr><b class="naranja">と</b><abbr title="あう - reunir">会った。</abbr> </p>
                              <p>- Reuní con profesor. </p> <br><br>




          <h1 id="sub-3">Las partículas de enumeración indeterminada 「や」 y 「とか」</h1>

                              <p>La partícula 「や」, como la partícula 「と」, se utiliza para hacer listas de dos o más nombres, con la diferencia de que es mucho más vaga que la partícula 「と」. Implica que puede haber otros elementos que no aparecen en la lista y, también, que no tiene uno que decidirse por todos los elementos que aparecen en la lista. En español lo consideraríamos un tipo de lista “y /o, etc.” </p>

                              <p>（１）　<abbr title="のみもの - bebida">飲み物</abbr><b class="naranja">や</b><abbr title="カップ - taza">カップ</abbr><b class="naranja">や</b><abbr title="ナプキン - servilleta">ナプキン</abbr>は、<abbr title="いる - necesitar">いらない</abbr>？</p>
                              <p>  - ¿Tu no necesitas (cosas como) bebidas, taza, servilleta, etc.?</p>
                              <p>（２）　<abbr title="くつ - zapatos">靴</abbr><b class="naranja">や</b><abbr title="シャツ - camisa">シャツ</abbr>を<abbr title="かう - comprar">買う。</abbr></p>
                              <p>- Compra (cosas como) zapatos, camisa etc.</p> <br><br>



          <h1 id="sub-4">La partícula 「の」</h1>

          <p>La partícula 「の」 tiene diversos usos, y es una partícula muy importante. La introducimos aquí porque, como las partículas 「と」 y 「や」, puede utilizarse para conectar uno o varios nombres. Veamos unos cuantos ejemplos. </p>
          <p>（１）　ボブ<b class="naranja">の</b><abbr title="ほん - libro">本</abbr>。- Libro de Bob.</p>
          <p>（２）　<abbr title="ほん - libro">本</abbr><b class="naranja">の</b>ボブ。- Bob de libro. </p>

          <p>La primera frase significa, básicamente, “El libro de Bob” (no estamos hablando de la Biblia). La segunda frase significa, "Bob de, el libro" que sería una errata. He traducido (1) como "El libro de Bob" porque la partícula 「の」 no siempre implica posesión, como se ve en el siguiente ejemplo: </p>
          <p>（１）　ボブは、<abbr title="アメリカ - USA">アメリカ</abbr><b class="naranja">の</b><abbr title="だいがく - universidad">大学</abbr><b class="naranja">の</b><abbr class="がくせい - estudiante">学生</abbr>だ。</p>
          <p>- Bob es estudiante de Universidad de América. </p>
          <p>La traducción al español de esto sería: "Bob es estudiante de una Universidad Americana”. El orden de modificación va a la inversa, de modo que Bob es un estudiante de una Universidad que es Americana. 「学生の大学のアメリカ」 significa "América de Universidad de estudiante" que está mal, ya que no tiene mucho sentido. (¿América de los estudiantes de la Universidad?).</p><br><br>


          <p>El nombre que se modifica se puede omitir si el contexto indica con claridad lo que se está omitiendo. Pueden omitirse las siguientes palabras redundantes que aparecen resaltadas.</p>

          <p>（１）　その<abbr title="シャツ - camisa">シャツ</abbr>は<abbr title="だれ - ¿quién?">誰</abbr>の<b class="naranja">シャツ</b>？- ¿Esta camisa es la camisa de quién?</p>
          <p>（２）　ボブの<b class="naranja"><abbr title="シャツ - camisa">シャツ</abbr></b>だ。- Es la camisa de Bob.</p>
          <p>que pasa a ser:</p>
          <p>（１）　その<abbr title="シャツ - camisa">シャツ</abbr>は<abbr title="だれ - ¿quién?">誰</abbr><b class="naranja">の</b>？- ¿De quien es esta camisa?</p>
          <p>（２）　ボブ<b class="naranja">の</b>だ。- Es la de Bob. </p>
          <p>（「その」 es una abreviación de 「それ + の」 y modifica directamente al nombre, porque la partícula 「の」 va intrínsecamente unida. También tenemos 「この」 de 「これの」 y 「あの」 de 「あれの」.） </p><br><br>

          <p>En este caso, lo que hace fundamentalmente la partícula 「の」 es reemplazar al nombre y adoptar ella misma el papel de nombre. Si añadimos la partícula 「の」 a los adjetivos y a los verbos podemos tratarles como si fueran nombres. La partícula se convierte entonces en un nombre genérico al que podemos tratar como a un nombre común. </p>
          <p>（１）　<b class="naranja"><abbr title="しろい - blanco">白い</abbr>の</b>は、<abbr title="かわいい - lindo/bonito">かわいい</abbr>。- La cosa que es blanca es bonita.</p>
          <p>（２）　<b class="naranja"><abbr title="じゅぎょう - clases">授業</abbr>に<abbr title="いく - ir">行く</abbr>の</b>を<abbr title="わすれる - olvidar">忘れた</abbr>。- Olvidó el acto de ir a clase.</p>
          <br><br>


          <p>Ahora podemos utilizar la partícula de objeto directo, la de tema y la que identifica, con verbos y adjetivos. En este caso no es necesario utilizar la partícula 「の」. Podemos utilizar el nombre 「物」, que es un objeto genérico, o 「こと」 si se trata de un hecho genérico. También podemos decir, por ejemplo: </p>
          <p>（１）　<abbr title="しろい - blanco">白い</abbr><b class="naranja"><abbr title="もの - cosa">物</abbr></b>は、<abbr title="かわいい - lindo/bonito">かわいい</abbr>。- Cosa que es blanca es bonita.</p>
          <p>（２）　<abbr title="じゅぎょう - clases">授業</abbr>に<abbr title="いく - ir">行く</abbr><b class="naranja">こと</b>を<abbr title="わすれる - olvidar">忘れた</abbr>。- Olvidó la cosa de ir a clase.</p><br><br>


          <p>Pero la partícula 「の」 es muy útil, ya que, con ella no es necesario especificar un nombre concreto. En los ejemplos siguientes la partícula 「の」 no reemplaza a ningún nombre en concreto, simplemente nos permite modificar oraciones subordinadas adjetivales y verbales como las oraciones nominales. La oración subordinada aparece resaltada. </p>

          <p>（１）　<b class="naranja"><abbr title="まいにち - todos los días">毎日</abbr><abbr title="べんきょう - estudiar">勉強</abbr><abbr title="する - hacer">する</abbr></b>のは<abbr title="たいへん - duro/dificil">大変。</abbr> - La cosa de estudiar todos los días es dura.</p>
          <p>（２）　<b class="naranja"><abbr title="まいにち - todos los días">毎日</abbr><abbr title="おなじ - lo mismo">同じ</abbr><abbr title="もの - cosa">物</abbr>を<abbr title="たべる">食べる</abbr></b>のは、<abbr title="おもしろい - interesante/divertido">面白くない。</abbr>- No es interesante comer misma cosa cada día. </p>
          <p>Puede que te hayas fijado en que la palabra 「同じ」 modifica directamente a 「物」, aunque es evidente que no se trata de un adjetivo-i. No tengo ni idea de porqué se puede hacer esto. Una posible explicación sería que, en realidad, se trate de un adverbio. Si este fuera el caso no sería necesario utilizar ninguna partícula, como veremos dentro de poco.</p><br><br>

          <p>Por otra parte, aunque se haya sustituido 「の」 por un nombre, en el caso de los adjetivos-na se necesita 「な」 para modificar al nombre . </p>
          <p>（１）　<abbr title="しずか - tranquilo">静か</abbr><b class="naranja">な</b><abbr title="へや - habitación">部屋</abbr>が、アリスの<abbr title="へや - habitación">部屋</abbr>だ。- Tranquila habitación es habitación de Alicia.</p>
          <p>Pasa a ser:</p>
          <p>（１）　<abbr title="しずか - tranquilo">静か</abbr><b class="naranja">な</b>のが、アリスの<abbr title="へや - habitación">部屋</abbr>だ。- La tranquila es habitación de Alicia.</p>




