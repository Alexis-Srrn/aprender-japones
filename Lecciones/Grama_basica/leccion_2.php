          <h1 id="sub-1">Definiendo las funciones gramaticales mediante partículas</h1>
          <p>Ahora queremos hacer buen uso de lo que llevamos aprendido asociando un nombre a otro nombre. Esto se hace con las llamadas partículas. Las partículas son uno o dos hiraganas que se sitúan al final de una palabra para definir qué función gramatical cumple dicha palabra en la frase. Es muy importante utilizar las partículas correctamente, porque el significado de una frase puede cambiar completamente cuando cambian las partículas. Por ejemplo, la frase: “come pez” puede convertirse en “el pez come” con solo cambiar una partícula.
        </p>




        <h1 id="sub-2">La partícula de tema 「は」</h1>

        <p>La primera partícula que vamos a aprender es la partícula de tema. La partícula de tema sirve fundamentalmente para identificar de qué es de lo que se está hablado, es decir, el tema de la frase. Pongamos que una persona dice: “no estudiante”. Ésta es una frase absolutamente válida en japonés, pero no nos dice gran cosa si no sabemos de qué trata la frase.</p>
        <p>La partícula de tema nos permite expresar de qué van nuestras frases. La partícula de tema es el carácter 「は」. Ahora bien, aunque este carácter se pronuncia normalmente /ha/ su pronunciación es /wa/ cuando se está utilizando como partícula de tema. </p>



        <p class="leccion">Ejemplo 1</p>
        <p>ボブ：　アリスは<abbr title="がくせい - estudiante">学生</abbr>？- (Alicia) ¿Eres estudiante?</p>
        <p>アリス：　うん、<abbr title="がくせい - estudiante">学生</abbr>。- aha, lo soy. </p>

        <p>Aquí Bob indica que la pregunta es sobre Alicia. Fíjate en que, aunque 「だ」 no aparece, en la traducción española aparecen las palabras “eres” y “soy”. Como sabemos que el tema es Alicia, no necesitamos saber más para adivinar que Alicia es una estudiante. </p>


        <p class="leccion">Ejemplo 2</p>

        <p>ボブ）　ジムは<abbr title="あした - mañana">明日</abbr>？- ¿Jim es mañana?</p>
        <p>アリス）　<abbr title="あした - mañana">明日</abbr>じゃない。- No es mañana.</p>
        <p>Al no haber contexto, no tenemos suficiente información para saber el significado de esta conversación. Es obvio que: “Jim es mañana” no tiene ningún sentido. Dentro de un contexto, siempre que tenga algo que ver con Jim y con mañana, la frase puede significar cualquier cosa. Puede tratar, por ejemplo, de cuándo tendrá lugar un examen.</p>

        <p class="leccion">Ejemplo 3</p>

        <p>アリス）　<abbr title="きょう - hoy">今日</abbr>は<abbr title="しけん - examen">試験</abbr>だ。- Hoy es examen.</p>
        <p>ボブ）　ジムは？ - ¿Y Jim?</p>
        <p>アリス）　ジムは<abbr title="あした - mañana">明日</abbr>。 - Jim es mañana (En cuanto a Jim, el examen es mañana) </p>

        <p>Necesitamos ser conscientes de lo genérico que puede llegar a ser el tema. El tema puede ser cualquier acción u objeto de cualquier parte, incluidas otras frases. En la última frase de la conversación anterior, por ejemplo, aunque la frase trata de cuándo tiene Jim el examen, la palabra examen ¡no aparece en toda la frase! </p>
        <p>Ya veremos, al final de esta lección, una partícula más específica que se vincula más firmemente a la frase, la partícula que identifica. </p>



        <h1 id="sub-3">La partícula de tema “que incluye” 「も」</h1>

        <p>Una partícula muy similar a la partícula de tema es la partícula de tema “que incluye”. En esencia se trata de la partícula de tema con el significado adicional de “también”. Su función, básicamente, es introducir otro tema además del tema que se esté tratando. La partícula de tema “que incluye” es el carácter 「も」. Su función se entiende mejor con un ejemplo. </p>

        <p class="leccion">Ejemplo 1</p>

        <p>ボブ：　アリスは<abbr title="がくせい - estudiante">学生</abbr>。？- (Alicia) ¿Eres estudiante?</p>
        <p>アリス：　うん、トムも<abbr title="がくせい - estudiante">学生</abbr>。- Aha, y Tom es estudiante también. </p>
        <p>Fíjate en que Alicia tiene que ser coherente con la inclusión. No tendría sentido decir “soy estudiante y Jim también no es estudiante” Si éste fuera el caso Alicia utilizará la partícula 「は」 para anular el significado de inclusión adicional, como aparece en el ejemplo siguiente. </p>

        <p class="leccion">Ejemplo 2</p>

        <p>ボブ：　アリスは<abbr title="がくせい - estudiante">学生</abbr>？- (Alicia) ¿Eres estudiante?</p>
        <p>アリス：　うん、でもトムは<abbr title="がくせい - estudiante">学生</abbr>じゃない。- Aha, pero Tom no es estudiante.</p>

        <p class="leccion">Ejemplo 3</p>

        <p>Y aquí tenemos otra posibilidad.</p>
        <p>ボブ：　アリスは<abbr title="がくせい - estudiante">学生</abbr>？- (Alicia) ¿Eres estudiante?</p>
        <p>アリス：　ううん、トムも<abbr title="がくせい - estudiante">学生</abbr>じゃない。- No, y Tom también no es estudiante. </p>

        <p>¿Y por qué Alicia habla de repente de Tom cuando Bob está preguntando por Alicia? Tal vez Tom esté de pié junto a ella y ella quiera incluirle en la conversación.</p>


        <h1 id="sub-4">La partícula que identifica 「が」</h1>

                <p>Ok, de modo que podemos identificar un nuevo tema utilizando las partículas 「は」 y 「も」. ¿Pero que pasa si no sabemos cual es el tema? ¿Qué pasa si lo que quiero preguntar es: “quién es el estudiante”? Lo que necesito es algún tipo de identificador, porque no sé quién es el estudiante.</p>
                <p>Aquí es donde entra en juego la partícula 「が」.</p>
                <p class="leccion">Ejemplo 1</p>

                <p>ボブ：　<abbr title="だれ - ¿quién?">誰</abbr>が<abbr title="がくせい - estudiante">学生</abbr>？- ¿Quién es el estudiante?</p>
                <p>アリス：　ジムが<abbr title="がくせい - estudiante">学生</abbr>。- Jim es el estudiante. </p>

                <p>Bob quiere identificar a quien es estudiante de entre todos los posibles candidatos. Alicia responde que es Jim. Fíjate en que Alicia podría haber utilizado también la partícula de tema para decir que ella sabe que Jim es un estudiante (posiblemente no el estudiante).</p>
                <p>Se puede ver la diferencia en el ejemplo siguiente.</p>



        <p class="leccion">Ejemplo 2</p>
              <p>（１）　<abbr title="だれ - ¿quién?">誰</abbr>が<abbr title="がくせい - estudiante">学生</abbr>？ - ¿Quién es el estudiante?</p>
              <p>（２）　<abbr title="がくせい - estudiante">学生</abbr>は<abbr title="だれ - ¿quién?">誰</abbr>？- ¿(El) estudiante es quién? </p>


          <p>Espero que quede claro que （１）pretende identificar a una persona específica como “estudiante” mientras que （２）sólo habla del estudiante. No se puede reemplazar 「が」con 「は」 en （１） porque "quién" se convertiría en el tema y la pregunta pasaría a ser "¿Es “quién” estudiante?"</p>
          <p>Las partículas 「は」 y 「が」 nos parecen muy similares debido a que es imposible traducir la diferencia al español. Por ejemplo, la traducción de 「私は学生」 y 「私が学生」 es: “yo soy estudiante” *. Pero sólo se parecen porque el español no puede expresar información sobre el contexto de forma tan concisa como lo hace a veces el japonés.</p>
          <p>En la primera frase 「私は学生」, como 「私」 es el tema, la frase significa "hablando de mí, soy estudiante". Sin embargo, en la segunda frase, 「私」 está especificando quién es el estudiante: 「学生」. Si nosotros queremos saber quién es el estudiante, la partícula 「が」 nos indica que es 「私」.</p>

