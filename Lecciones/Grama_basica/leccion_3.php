          <h1 id="sub-1">Propiedades de los Adjetivos</h1>
          <p>Ahora que somos capaces de unir de varias formas dos nombres por medio de partículas, queremos describir nuestros nombres mediante adjetivos. Un adjetivo puede modificar directamente a un nombre que venga inmediatamente después de él. También puede, como sucedía con los nombres, unirse a él mediante partículas. Cualquier adjetivo pertenece a uno de los dos grupos siguientes: los Adjetivos-na y los Adjetivos-i. Vamos a ver qué les diferencia y su comportamiento dentro de la frase.
        </p>



        <h1 id="sub-2">El adjetivo-na</h1>

        <p>El adjetivo –na es muy fácil de aprender porque se comporta, esencialmente, como un nombre. De hecho, ambos se parecen mucho. Vamos a considerar que su comportamiento es el mismo a menos que yo indique expresamente que hay alguna diferencia. Una diferencia importante es que los adjetivos-na pueden modificar directamente a un nombre que va detrás colocando 「な」entre el adjetivo y el nombre (de ahí su nombre, adjetivo-na). </p>

        <p>Primero, para formar el pasado negativo solo hay que añadir 「じゃない」 al nombre o adjetivo-na.</p>

        <p>（１）　<abbr title="しずか - tranquilo">静か</abbr><b>な</b><abbr title="ひと - persona">人</abbr>。- Persona tranquila (Tranquila persona).</p>

        <p>Además de modificar directamente al nombre, para lo que se necesita un 「な」, se podría decir también que un nombre es un adjetivo utilizando la partícula de tema o la que identifica en una frase con la estructura [Nombre] [Partícula] [Adjetivo] (por ejemplo 「人は静か」） Esto es esencialmente lo mismo que el estado-de-ser de los nombres, del que hemos tratado en las dos secciones anteriores. </p>

        <p>（１）　<abbr title="ともだち - amigo">友達</abbr>は<abbr title="しんせつ - amable">親切</abbr>。- Amigo es amable.</p>
        <p>（２）　<abbr title="ともだち - amigo">友達</abbr>は<abbr title="しんせつ - amable">親切</abbr>な<abbr title="ひと - persona">人</abbr>。- Amigo es amable persona. </p>

        <p>¿Te acuerdas de que los adjetivos –na se comportan casi exactamente igual que los nombres? Pues lo puedes ver en los siguientes ejemplos: </p>

        <p>（１）　ボブは<abbr title="さかな - pez">魚</abbr>が<abbr title="すき - gustar"><b class="naranja">好きだ</b></abbr>。- A Bob le gusta el pescado.</p>

        <p>（２）　ボブは<abbr title="さかな - pez">魚</abbr>が<abbr title="すき - gustar"><b class="naranja">好きじゃない</b></abbr>。- A Bob no le gusta el pescado.</p>

        <p>（３）　ボブは<abbr title="さかな - pez">魚</abbr>が<abbr title="すき - gustar"><b class="naranja">好きだった</b></abbr>。- A Bob le gustaba el pescado.</p>

        <p>（４）　ボブは<abbr title="さかな - pez">魚</abbr>が<abbr title="すき - gustar"><b class="naranja">好きじゃなかった</b></abbr>。- A Bob no le gustaba el pescado. </p>
        <p>¿Te resulta familiar la conjugación? Debería, si prestaste atención cuando hablamos de la conjugación del estado-de-ser relativa a los nombres. Si te confunde que en japonés “gustar” sea un adjetivo y no un verbo, piensa que 「好き」 significa “deseable”. </p>

        <p>También se pueden utilizar las tres últimas conjugaciones para modificar directamente al nombre (recuerda poner 「な」 en las frases positivas que no estén en pasado).</p>


        <p>（１）　<abbr title="さかな - pez">魚</abbr>が<abbr title="すき - gustar"><b class="naranja">好きな</b></abbr>タイプ。- El tipo al que le gusta el pescado.</p>
        <p>（２）　<abbr title="さかな - pez">魚</abbr>が<abbr title="すき - gustar"><b class="naranja">好きじゃない</b></abbr>タイプ。- El tipo al que no le gusta el pescado.</p>
        <p>（３）　<abbr title="さかな - pez">魚</abbr>が<abbr title="すき - gustar"><b class="naranja">好きだった</b></abbr>タイプ。- El tipo al que le gustaba el pescado.</p>
        <p>（４）　<abbr title="さかな - pez">魚</abbr>が<abbr title="すき - gustar"><b class="naranja">好きじゃなかった</b></abbr>タイプ。- El tipo al que no le gustaba el pescado. </p>


        <p>Aquí la oración entera 「<abbr title="さかな - pez">魚</abbr>が<abbr title="すき - gustar">好き</abbr>」、
          「<abbr title="さかな - pez">魚</abbr>が<abbr title="すき - gustar">好きじゃない</abbr>」、 etc. modifica "tipo"
          para hablar sobre los tipos (de personas) a los que les gusta o no les gusta el pescado.
           Puede verse por qué es útil este tipo de frase, 「タイプは<abbr title="さかな - pez">魚</abbr>が<abbr title="すき - gustar">好きだ</abbr>。」 significaría
           “al tipo le gusta el pescado”, que no tiene mucho sentido. </p>
           <p>Podemos incluso tratar a la frase nominal descriptiva completa como si fuese nada mas que un nombre. Por ejemplo, podemos hacer que la frase entera sea un tema, como en el ejemplo siguiente: </p>
           <p></p>
           <p>（１）　<b class="naranja"><abbr title="さかな - pez">魚</abbr>が<abbr title="すき - gustar">好き</abbr>じゃないタイプは</b>、
             <abbr title="にく - carne">肉</abbr>が<abbr title="すき - gustar">好き</abbr>だ。</p>
             <p> - A los tipos (de personas) a los que no les gusta el pescado les gusta la carne. </p>
             <br><br>

            <h1 id="sub-3">El adjetivo-i</h1>

            <p>El adjetivo-i se llama así porque siempre termina por el carácter hiragana 「い」. Se trata del okurigana, y es la parte que cambia cuando se conjuga el adjetivo. Pero puedes encontrar adjetivos-na que terminen también por 「い」, como es el caso de 「きれい（な）」. Entonces ¿cómo se pueden diferenciar?</p>
            <p>La mala noticia es que no hay ningún método seguro para hacerlo. Pero la buena noticia es que solo se me ocurren dos ejemplos de adjetivos-na que terminen por 「い」 y que se suelan escribir en hiragana: 「きれい」 y 「嫌い」. Que yo recuerde, los demás adjetivos-na que terminan por 「い」 se suelen escribir en kanji, lo cual nos indica que no son adjetivos-i. </p>
            <p>Por ejemplo, 「きれい」, que en kanji se escribe 「<abbr title="きれい - lindo/hermoso">綺麗</abbr>」 o 「<abbr title="きれい - lindo/hermoso">奇麗</abbr>」, sabemos que no puede ser un adjetivo-i porque la 「い」 de 「麗」 está contenida en el kanji.</p>
            <p> Y es que el papel principal de la 「い」 en los adjetivos-i, es que sea posible conjugarlos sin que ello afecte al kanji. </p>
            <p> Hay dos reglas nuevas para conjugar los adjetivos-i. Para negar o formar el pasado quitamos primero la 「い」 y luego añadimos 「くない」 para la negación o 「かった」 para el pasado. Como 「くない」 termina en 「い」, se puede tratar a la forma negativa como si fuese un adjetivo-i. Por lo tanto, se utiliza la misma regla para cambiar a pasado negativo o a pasado positivo.</p>


            <p class="leccion">Reglas de conjugación para los adjetivos-i:</p>

            <p><b>Negativa:</b> Primero se quita la 「い」final del adjetivo-i y luego se añade 「くない」</p>
            <p> 例）<abbr title="たかい - alto">高</abbr><b class="naranja">い</b> → <abbr title="たかい - alto">高</abbr><b class="naranja">くない</b></p>


            <p><b>Pasado:</b> Primero se quita la 「い」final del adjetivo-i o del adjetivo-i negativo y luego se añade「かった」</p>
            <p>例）　<abbr title="たかい - alto">高</abbr><b class="naranja">い</b> → <abbr title="たかい - alto">高</abbr><b class="naranja">かった</b></p>
            <p>例）　<abbr title="たかい - alto">高くな</abbr><b class="naranja">い</b> → <abbr title="たかい - alto">高くな</abbr><b class="naranja">かった</b></p>

            <br>
            <img src="../../img/lecciones/3-leccion/tabla-1.png" alt="tabla-1">
            <br><br><br>


          <p>  Puedes modificar directamente al nombre con solo unir el nombre al adjetivo.</p>
          <p>  （１）　例）　<abbr title="たかい - alto"><b class="naranja">高い</b></abbr>ビル。- Edificio alto.</p>
          <p>  （２）　例）　<abbr title="たかい - alto"><b class="naranja">高くない</b></abbr>ビル。- Edificio que no es alto.</p>
          <p>  （３）　例）　<abbr title="たかい - alto"><b class="naranja">高かった</b></abbr>ビル。- Edificio que era alto.</p>
          <p>  （４）　例）　<abbr title="たかい - alto"><b class="naranja">高くなかった</b></abbr>ビル。- Edificio que no era alto.</p>
          <p>  También se pueden encadenar varios adjetivos en cualquier orden y de cualquier forma.</p>
          <p>  （１）　<b class="naranja"><abbr title="しずか - tranquilo">静か</abbr>な<abbr title="たかい - alto">高い</abbr></b>ビル。- Un edificio tranquilo y alto.</p>
          <p>（２）　<b class="naranja"><abbr title="たかい - alto">高くない</abbr><abbr title="しずか - tranquilo">静か</abbr>な</b>ビル。- Un edificio no alto, tranquilo. </p>

          <p>Verás que se puede construir el mismo tipo de frase nominal que se podía construir con los adjetivos-na, la única diferencia es, naturalmente, que no necesitamos poner 「な」 para modificar directamente al nombre. En el ejemplo siguiente, la frase descriptiva 「値段が高い」 modifica directamente a 「レストラン」. </p>
          <p>（１）　<b class="naranja"><abbr title="ねだん - precios">値段</abbr>が<abbr title="たかい - alto">高い</abbr>レストラン</b>は
            <abbr title="あまり - no mucho">あまり</abbr><abbr title="すき - gustar">好きじゃない</abbr>。</p>
          <p>- No me gustan los restaurantes muy caros. </p><br><br>



        <h1 id="sub-4">Una molesta excepción</h1>

        <p>Hay un adjetivo-i que significa “bueno” y actúa de forma ligeramente diferente al resto de los adjetivos-i. Este es un claro ejemplo de que aprender japonés es más difícil para los principiantes, porque las frases más comunes y útiles son las que presentan más excepciones.
        La palabra “bueno” era, en un principio, 「よい（<abbr title="よい - bueno">良い</abbr>）」, pero con el tiempo se transformó en 「いい」.Cuando se escribe con kanji se suele leer 「よい」, de modo que 「いい」 se escribe casi siempre en hiragana.
        Por ahora todo bien. Desgraciadamente, todas las conjugaciones han derivado de 「よい」 y no de 「いい」. Esto se ve en la tabla que aparece más abajo. </p>

        <p>Otro adjetivo que se comporta así es かっこいい, porque es una versión abreviada de dos palabras que aparecieron juntas: 「格好」 y 「いい」. Como utiliza el mismo 「いい」, hay que conjugarlo del mismo modo. </p>

          <br>
            <img src="../../img/lecciones/3-leccion/tabla-2.png" alt="tabla-2">
            <br><br>

            <p>Hay que conjugar a partir de 「よい」 y no de 「いい」.</p>
            <p class="leccion">Ejemplos:</p>
            <p>（１）　<abbr title="ねだん - precios">値段</abbr>が<abbr title="あんまり - no mucho">あんまり</abbr><abbr title="いい - bueno"><b class="naranja">よくない。</b></abbr></p>
            <p>-El precio no es muy bueno.</p>

            <p>（２）　<abbr title="かれ - Él/novio">彼</abbr>は<abbr title="かっこいい - genial/cool"><b class="naranja">かっこよかった</b></abbr>！</p>
          <p>  - ¡Parecía muy genial! </p>



