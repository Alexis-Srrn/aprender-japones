          <h1 id="sub-1">El papel de los Verbos</h1>

          <p>Ya hemos visto algunas formas de describir nombres por medio de otros nombres y de adjetivos. Esto nos da ya bastante capacidad de expresión. Sin embargo, todavía no sabemos expresar acciones. Aquí es donde entran en escena los verbos. En japonés, el verbo aparece siempre al final de la frase. Como todavía no hemos aprendido a formar frases de más de una oración, esto significa, por ahora, que cualquier frase en la que aparezca un verbo debe terminar por dicho verbo. Ahora vamos a ver las dos categorías principales en que se dividen los verbos, lo que nos va a permitir definir reglas muy precisas para conjugar. Antes de comenzar con el estudio de los verbos hay algo importante a tener en cuenta. </p>

          <br> <p><b style="color:red">Para que una frase sea gramaticalmente completa solo se necesita un verbo (incluyendo el estado-de-ser).</b></p><br>

          <p>O, dicho de otro modo, a diferencia del español, lo único que se necesita para construir una frase gramaticalmente completa es un verbo ¡nada más! Para entender el japonés es imprescindible entender esta regla fundamental. Ésta es la razón de que no puedan traducirse al español ni las frases japonesas más simples. Cualquier conjugación se hace a partir de la forma de diccionario (la forma que aparece en el diccionario). </p>
          <p>  Una frase gramaticalmente completa:</p>
          <p>（１）　<abbr title=" たべる - comer">食べる</abbr>。- Comer. (Entre las posibles traducciones tenemos: Yo como / ella come / ellos comen) </p>



        <h1 id="sub-2">Dividiendo los Verbos en verbos-ru y verbos-u</h1>

        <p>Casi todos los verbos japoneses se pueden agrupar en una de dos categorías: verbos-ru （一段動詞） y verbos-u （五段動詞）. Los dos únicos verbos que no se considera que estén en ninguna de estas dos categorías son 「する」 que significa 'hacer' y 「来る」 que significa 'llegar'. Por otra parte, las reglas para conjugar son casi las mismas, y dependen del grupo al que pertenezca el verbo. Saber a qué grupo pertenece cada verbo es bastante fácil.</p>

        <p>Recuerda, todos los verbos tienen una cadena de kanas, los okurigana, que se modifica al conjugar el verbo. Pues bien, si al convertir el verbo en caracteres romanos (llamados 「「ローマ字」 en japonés) éste termina en "iru" o "eru", entonces es un verbo -ru. Por ejemplo, 「食べる」 romanizado es "taberu" y como termina por 'eru', es un verbo -ru. Otro ejemplo de verbo -ru es 「起きる」 que romanizado es "okiru". El resto de los verbos que no terminan por "iru" o "eru" son verbos -u.</p>

        <p>Pero hay un pequeño inconveniente. Los verbos –ru terminan siempre por 「る」 y los verbos –u por el sonido vocal / u /. Esto, desgraciadamente, incluye 「る」 además de 「つ」、「す」、「く」、「ぐ」、「む」、「ぶ」、「う」 y 「ぬ」 （「死ぬ」 es el único verbo terminado en 「ぬ」）. Incluso aunque un verbo termine por 「る」, si no termina por "iru" o por "eru" es un verbo -u. Pero hay una serie de verbos -u que terminan por "iru" o "eru" y no hay forma de distinguirlos de los verbos -ru. Lo único que se puede hacer es aprendérselos de memoria. Al final de esta lección puedes encontrar un apéndice con una lista de estos verbos. </p>

        <p>Dado lo consistente de las reglas, con la práctica los verbos -u te empezarán a “sonar” como verbos -u, y lo mismo sucederá con los verbos –ru. A partir de un determinado momento sabrás la categoría a la que pertenecen los verbos nuevos con los que te encuentres tal cual, sin tener que pararte a pensarlo dos veces. Al menos eso es lo que se pretende. </p>

        <p class="leccion">Cómo clasificar verbos -ru y verbos -u</p>
        <ul>
          <li>Si no termina en iru/eru → verbo -u</li>
          <li>Si termina en iru/eru → verbo -ru con alguna excepción.</li>
        </ul><br><br><br>



