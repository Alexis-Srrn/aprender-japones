                    <p class="leccion">Ejemplos</p>
                    <p>Aquí tienes algunas frases de ejemplo en las que aparecen verbos-ru, verbos-u y excepciones.</p>
                    <p>（１）　アリスは<b class="naranja"><abbr title="たべる - comer" >食べる</abbr>。</b>- Alicia come.</p>
                    <p>（２）　ジムが<b class="naranja"><abbr title="あそぶ - jugar" >遊ぶ</abbr>。</b>- Jim es el que juega.</p>
                    <p>（３）　ボブも<b class="naranja"><abbr title="する - hacer">する</abbr>。</b>- Bob también lo hace.</p>
                    <p>（４）　お金が<b class="naranja"><abbr title="ある - existir/haber">ある</abbr>。</b>- Hay dinero. (lit: Con respecto al dinero, existe.)</p>
                    <p>（５）　私は<b class="naranja"><abbr title="かう - comprar">買う</abbr>。</b>- Con respecto a mí, compra.</p>
                    <p>（６）　猫は<b class="naranja"><abbr title="いる - existir/haber">いる</abbr>。</b>- Hay gato. (lit: Con respecto al gato, existe.)</p> <br> <br>


