          <p class="leccion">Ejemplos:</p>


          <p>（１）　アリスは<abbr title="たべる - comer"><b class="naranja">食べない</b></abbr>。- Con respecto a Alicia, no come.</p>
          <p>（２）　ジムが<abbr title="あそぶ - jugar"><b class="naranja">遊ばない</b></abbr>。- Jim es el que no juega.</p>
          <p>（３）　ボブも<abbr title="する - hacer" ><b class="naranja">しない</b></abbr>。- Bob también no lo hace.</p>
          <p>（４）　お金が<abbr title="ある - existir (inanimado)"><b class="naranja">ない</b></abbr>。- No hay dinero.</p>
          <p>（５）　私は <abbr title="かう - comprar"><b class="naranja">買わない</b></abbr>。- Con respecto a mí, no compro.</p>
