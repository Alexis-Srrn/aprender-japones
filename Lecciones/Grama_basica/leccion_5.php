          <h1 id="sub-1">La negación de los verbos</h1>
          <p>Una vez visto lo que hay que hacer para declarar las cosas y para realizar acciones con verbos, queremos ser capaces de hablar negando. Queremos decir que tal o cual acción no se realizó. La negación de los verbos se consigue conjugándolos, como el estado-de-ser de los nombres y de los adjetivos. Pero las reglas son un poco más complicadas. </p>


          <h1 id="sub-2">Conjugando los verbos a su forma negativa</h1>
          <p>Vamos a partir de la clasificación de los verbos para establecer reglas de conjugación, pero antes de hacerlo tenemos que hablar de una importante excepción a la regla para conjugar a la forma negativa, me refiero al verbo 「 <abbr title="ある - existir/haber">ある</abbr>」. 「<abbr title="ある - existir/haber">ある</abbr>」 es un verbo-u que se utiliza para expresar la existencia de objetos inanimados.</p>

          <p>Pongamos un ejemplo: si queremos decir que hay una silla en la habitación, utilizaremos el verbo 「<abbr title="ある - existir/haber">ある</abbr>」. El verbo equivalente para objetos animados (como gente o animales) es 「<abbr title="いる - existir/haber">いる</abbr>」, que es un verbo-ru normal. Si lo que queremos es decirle a alguien que había una persona en la habitación, tendremos que utilizar el verbo 「<abbr title="いる - existir/haber">いる</abbr>」 y no el verbo 「 <abbr title="ある - existir/haber">ある</abbr>」. Estos dos verbos 「 <abbr title="ある - existir/haber">ある</abbr>」 e 「<abbr title="いる - existir/haber">いる</abbr>」 son muy distintos de todos los demás verbos, ya que no representan una acción, representan simplemente la existencia. Además hay que molestarse en elegir el que sea adecuado dependiendo de si se trata de objetos animados o inanimados.</p>

          <p>En cualquier caso, la razón por la que saco aquí este tema es que la forma negativa de 「<abbr title="ある - existir/haber">ある</abbr>」 (que significa que algo no existe) es 「ない」. Recuerda, se trata de una excepción, así que no utilices con 「<abbr title="ある - existir/haber">ある</abbr> 」 las reglas de negación estándar.</p>


          <p class="leccion">La forma negativa de 「ある」 es 「ない」.</p>

            <p>Y ahora las reglas para todos los demás verbos. Para formar la negación de un verbo-ru solo hay que quitar 「る」 y añadir 「ない」. En el caso de los verbos-u puede ayudar ver la versión romanizada del verbo, solo hay que quitar el sonido vocal / u / y añadir "anai". O, mejor haz lo siguiente: ve a la tabla de hiragana y haz lo siguiente: dado el último carácter hiragana, que estará siempre en la fila de la / u /, sube desde la columna que sea hacia arriba y reemplázalo con el carácter que aparece en la fila de la / a /. Por ejemplo 「く」 cambia a 「か」.</p>

            <p>Una importante excepción a esta regla son los verbos que terminan por 「う」. Hay que sustituir la 「う」 por una 「わ」, y no por una 「あ」, que sería lo que correspondería según la regla. También hay que memorizar las conjugaciones de los dos verbos que son excepción y de 「ある」. En la tabla que viene a continuación aparece la conjugación de algunos verbos–ru, de algunos verbos–u y de los verbos que son una excepción.</p>

            <p class="leccion">Cómo poner los verbos en forma negativa</p>
                  <ul>
                    <li>verbos-ru: Para conjugar los verbos-ru a su forma negativa se quita la última 「る」 y a esto se le añade 「ない」. </li>
                        <p>例）<abbr title="みる - observar">見<b class="naranja">る</b></abbr> → <abbr title="みる - observar">見<b class="naranja">ない</b></abbr></p>
                        <p>例）<abbr title="でる  - salir">出<b class="naranja">る</b></abbr> → <abbr title="でる  - salir">出<b class="naranja">ない</b></abbr></p>
                    <li>verbos-u: Para conjugar los verbos-u a su forma negativa primero se reemplaza la / u / final por una / a / y a esto se le añade 「ない」. </li>
                        <p>例）　<abbr title="のむ - beber">飲<b class="naranja">む</b></abbr> → <abbr title="のむ - beber">飲<b class="naranja">ま</b></abbr> → <abbr title="のむ - beber">飲ま</abbr><b class="naranja">ない</b></p>
                        <p>例）　<abbr title="まつ - esperar">待<b class="naranja">つ</b></abbr> → <abbr title="まつ - esperar">待<b class="naranja">た</b></abbr> → <abbr title="まつ - esperar">待た</abbr><b class="naranja">ない</b></p>
                    <li>Una excepción importante son los verbos que terminan por 「う」. Con ellos hay que reemplazar la 「う」 por 「わ」 （no 「あ」） y a esto añadirle 「ない」. </li>
                        <p>例）　<abbr title=" ひろう - recoger">拾<b class="naranja">う</b></abbr> → <abbr title=" ひろう - recoger">拾</abbr><b class="naranja">わ</b> → <abbr title=" ひろう - recoger">拾わ</abbr><b class="naranja">ない</b></p>
                  </ul><br><br>

