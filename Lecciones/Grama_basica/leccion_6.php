          <h1 id="sub-1">Poniendo los verbos en pasado</h1>

        <p class="leccion">Declarando que algo es así usando 「だ」</p>

        <p>Vamos a terminar con las propiedades básicas de los verbos aprendiendo a formar el pasado y el pasado negativo de las acciones. Antes de ello te quiero avisar de que las reglas de conjugación de esta sección van a ser lo más difícil que vas a tener que aprender durante el estudio del japonés. Superada esta etapa, las reglas de conjugación que vayan apareciendo te parecerán fáciles, aunque tendrás que volver sobre esta sección varias veces antes de que consigas asimilar todas las reglas. Seguramente vas a tener que practicar mucho antes de que consigas familiarizarte con todas las conjugaciones. </p>



        <h1 id="sub-2">Pasado de los verbos -ru</h1>

        <p>Vamos a empezar con los verbos –ru, que son los más fáciles. Para pasar un verbo –ru de su forma de diccionario al pasado solo hay que quitar 「る」 y añadir 「た」. </p>

        <p class="leccion">Para poner los verbos –ru en pasado </p>

        <p>En los verbos –ru, quita 「る」 y añade 「た」</p>
        <p>例）<abbr title="でる - salir">出<b class="naranja">る</b></abbr> → <abbr title="でる - salir">出</abbr><b class="naranja">た</b></p>
        <p>例）<abbr title="すてる - lanzar">捨て<b class="naranja">る</b></abbr> → <abbr title="すてる - lanzar">捨て</abbr><b class="naranja">た</b></p>

        <p class="leccion">Ejemplos</p>
        <p>（１）　<abbr title="ごはん - comida">ご飯</abbr>は、<abbr title="たべる - comer"><b class="naranja">食べた</b></abbr>。- Con respecto a la comida, comí.</p>

        <p>（２）　<abbr title="えいが - películas">映画</abbr>は、<abbr title="ぜんぶ - todos/todas">全部</abbr><abbr title="みる - ver"><b class="naranja">見た</b></abbr>。- Con respecto a las películas, las vi todas. </p> <br><br>




