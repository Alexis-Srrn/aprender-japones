        <h1 id="sub-4">El Pasado negativo de los verbos</h1>
          <p>Las reglas para formar el pasado negativo son las mismas para todos los verbos. Te habrás dado cuenta de que la forma negativa de casi todo termina siempre por 「ない」. La regla para formar el pasado negativo de los verbos es la misma que la de las demás formas negativas que terminan por 「ない」. Partiendo de la forma negativa que cualquier verbo se quita la 「い」 de la terminación 「ない」 y se sustituye por 「かった」.</p>
          <p class="leccion">Para poner los verbos en pasado negativo </p>

          <p>Pon primero el verbo en su forma negativa y reemplaza la 「い」 por 「かった」</p>
          <p>例）<abbr title="すてる - lanzar">捨て<b class="naranja" class="naranja">る</b></abbr> → <abbr title="すてる - lanzar">捨てな</abbr><b class="naranja">い</b> → <abbr title="すてる - lanzar">捨てな</abbr><b class="naranja">かった</b></p>
          <p>例）<abbr title="いく - ir">行<b class="naranja" class="naranja">く</b></abbr> → <abbr title="いく - ir">行かな</abbr><b class="naranja">い</b> → <abbr title="いく - ir">行かな</abbr><b class="naranja">かった</b></p>


        <p class="leccion">Ejemplos</p>

        <p>  （１）　アリスは<abbr title="たべる - comer">食べな</abbr><b class="naranja">かった</b>。
       - Alicia no comió.</p>

        <p>  （２） ジムが<abbr title="する - hacer" >しな</abbr><b class="naranja">かった</b>。
          - Jim es el que no lo hizo.</p>

        <p>  （３）　ボブも<abbr title="いく - ir">行かな</abbr><b class="naranja">かった</b>。
           - Bob también no fue.</p>

        <p>  （4）　<abbr title="わたし - Yo">私</abbr>は<abbr title="かう -  comprar"><b class="naranja">買わなかった</b></abbr>
          - Con respecto a mí, no compré.</p>
