          <h1 id="sub-1">Partículas utilizadas con los verbos</h1>
                          <p>En esta sección vamos a aprender algunas partículas nuevas que son esenciales para poder usar los verbos. Básicamente aprenderemos a establecer cuál es el objeto directo de un verbo y el lugar en que se ejecuta el verbo (ya sea físicamente o de forma abstracta). </p>


          <h1 id="sub-2">La partícula de objeto directo 「を」</h1>

                          <p>La primera partícula que vamos a aprender es la partícula de objeto, porque es una partícula muy sencilla. El carácter 「を」 se une al final de una palabra para indicar que esta palabra es el objeto directo del verbo. Este carácter no se utiliza, en principio, en ninguna otra parte. A ello se debe que no se utilice casi nunca su equivalente katakana 「ヲ」, porque las partículas se escriben siempre en hiragana. El carácter 「を」, aunque técnicamente se pronuncia /uo/, en la conversación real suena /o/. A continuación veremos algunos ejemplos de la partícula de objeto directo en acción. </p>

                          <p>（１）　<abbr title="さかな - pescado"> 魚</abbr><b class="naranja">を</b><abbr title=" たべる - comer">食べる</abbr>。 - Comió pescado. </p>
                          <p>（２）　ジュース<b class="naranja">を</b><abbr title="のむ - beber">飲んだ</abbr>。 - Bebió zumo.</p>

                          <p>A diferencia del español, en japonés los lugares pueden ser el objeto directo de verbos de movimiento tales como 「 歩く 」 y 「 走る 」. Como el verbo de movimiento se dirige <b>hacia</b> el lugar, el concepto de objeto directo es el mismo en japonés. Pero, como puede verse en los ejemplos siguientes, muchas veces se traduce al español a algo un poco diferente, debido a la ligera diferencia del concepto de objeto directo.</p>

                          <p>（３）　 街 を ぶらぶら 歩く 。- Pasear por la ciudad sin dirección. (Lit: Sin dirección pasear ciudad)</p>
                          <p>（４）　 高速 道路 を 走る 。 - Correr por la autopista. (Lit: Correr autopista)</p>

                          <p>  Cuando se usa 「 する 」 con un nombre, la partícula 「を」 es opcional y se puede tratar al conjunto [nombre + する ] como si fuese un verbo.</p>
                          <p>  （１）　 毎日 、 日本語 を 勉強 する 。- Estudiar japonés todos los días.</p>
                          <p>（２）　 メールアドレス を 登録 した 。- Registrada la dirección e-mail. </p>


          <h1 id="sub-3">La partícula de objetivo 「に」</h1>

                              <p>La partícula 「に」 puede determinar el objetivo de un verbo. Es diferente de la partícula 「を」, en la que el verbo hace algo al objeto directo. En el caso de la partícula 「に」, el verbo hace algo hacia la palabra asociada con la partícula 「に」. Por ejemplo, mediante la partícula 「に」 se especifica el objetivo de un verbo de movimiento .</p>

                              <p>（１）　ボブは<abbr title="にほん - Japón">日本</abbr><b class="naranja">に</b><abbr title="いく - ir">行った</abbr>。 - Bob fue a Japón.</p>
                              <p>（２）　<abbr title="いえ - casa">家</abbr><b class="naranja">に</b><abbr title="かえる - volver/regresar">帰らない</abbr>。- No volver a casa.</p>
                              <p>（３）　<abbr title="へや - habitación">部屋</abbr><b class="naranja">に</b><abbr title="くる - venir/llegar">くる</abbr>。- Llegar a la habitación. </p>



                              <br> <br>
                              <p>Como el objetivo del verbo de movimiento 'llegar' no es “hacia”, sino más bien “desde”, utilizar 「に」 significaría "llegar a". Para decir, "venir desde" habría que utilizar 「から」 (que significa "desde") en lugar de 「に」. También va emparejado a menudo con 「まで」, que significa 'hasta'.</p>
                             <p>（１）　アリスは、<abbr title="アメリカ - America/USA">アメリカ</abbr><b class="naranja">から</b><abbr title="くる - venir">きた</abbr>。 - Alicia vino desde América.</p>
                             <p>（２）　<abbr title="しゅくだい - deberes">宿題</abbr>を<abbr title="きょう - hoy">今日</abbr><b class="naranja">から</b><abbr title="あした - mañana">明日</abbr><b class="naranja">まで</b><abbr title="する - hacer">する</abbr>。- Hacer los deberes de hoy a mañana.</p>
                             <br> <br>

                             <p>En japonés la noción de objetivo es muy común, y no se limita a los verbos de movimiento. Por ejemplo, la localización de un objeto se considera el objetivo de un verbo de existencia（ある e いる）. Los espacios de tiempo (la hora, la fecha, etc.) son también uno de los objetivos más comunes. Aquí pongo algunos ejemplos de verbos que no son de movimiento y de sus objetivos.</p>

                             <p>（１）　<abbr title="ねこ - gato">猫は</abbr><abbr title="へや - habitación">部屋</abbr><b class="naranja">に</b>いる。- El gato está en habitación.</p>
                             <p>（２）　<abbr title="いす - silla">椅子</abbr>が<abbr title="だいどころ - cocina">台所</abbr><b class="naranja">に</b><abbr title="ある - existir">あった</abbr>。- La silla estaba en la cocina.</p>
                             <p>（３）　<abbr title="いい - bueno">いい</abbr><abbr title="ともだち - amigo">友達</abbr><b class="naranja">に</b><abbr title="あう - reunirse con">会った</abbr>。- Reuní con buen amigo.</p>

                             <p>No olvidemos utilizar 「ある」 para objetos inanimados, como la silla, e 「いる」para objetos animados, como el gato. </p>



                             <br><br>


          <h1 id="sub-4">La partícula de dirección 「へ」</h1>


                              <p>Aunque 「へ」 se pronuncia normalmente /he/, cuando se utiliza como partícula siempre se pronuncia /e/ （え）. La diferencia fundamental entre las partículas 「に」 y 「へ」 es que 「に」 va a un objetivo que es el destino final que se quiere alcanzar (ya sea físicamente o en abstracto). La partícula 「へ」 por su parte, se utiliza para expresar el hecho de que uno está saliendo en dirección a la meta. En consecuencia, solo se utiliza con verbos de movimiento que implican seguir una dirección. Tampoco determina que el objetivo sea el destino final al que se quiere llegar, solo que uno va en esa dirección. En otras palabras, la partícula 「に」 se asocia al destino mientras que la partícula 「へ」 no deja claro el lugar al que uno se dirige en última instancia. Por ejemplo, si decidimos reemplazar 「に」 por 「へ」 en los tres primeros ejemplos de la sección anterior, se produce un ligero cambio de matiz. </p>


                              <p>（１）　ボブは<abbr title="にほん - Japón">日本</abbr><b class="naranja">へ</b><abbr title="いく - ir">行った</abbr>。- Bob se dirigía a Japón.</p>
                              <p>（２）　<abbr title="いえ - casa">家</abbr><b class="naranja">へ</b><abbr title="かえる - volver/regresar">帰らない</abbr>。- No ir hacia casa.</p>
                              <p>（３）　<abbr title="へや - habitación">部屋</abbr><b class="naranja">へ</b><abbr title="くる - venir">くる</abbr>。- Ir hacia habitación.</p>
                              <br><br>


          <h1 id="sub-5">La partícula de contexto 「で」</h1>
                            <p>La partícula 「で」 nos va a permitir especificar el contexto en el que la acción tiene lugar.
                            Por ejemplo, si una persona se come un pescado ¿donde se lo come? Si una persona ha ido a la escuela
                            ¿con que medio fue? ¿Con qué te tomas la sopa? Todas estas preguntas pueden responderse con la partícula
                            「で」. A continuación veremos algunos ejemplos.</p>

                            <p>（１）　<abbr title="えいがかん - cine">映画館<b class="naranja">で</b><abbr title="みる - ver">見た</abbr>。- Lo ví en un cine.</p>
                            <p>（２）　<abbr title="バス - bús">バス</abbr><b class="naranja">で</b><abbr title="かえる - volver a casa">帰る</abbr>。- Volver a casa con el autobús.</p>
                            <p>（３）　<abbr title="レストラン - restaurante">レストラン</abbr><b class="naranja">で</b><abbr title="ひるごはん - almuerzo">昼ご飯</abbr>を<abbr title="たべた - comer">食べた</abbr>。- Comí el almuerzo en el restaurante.</p>



                            <p>Puede servir de ayuda pensar en 「で」 como que significa "por medio de".
                            Si lo hacemos obtendremos el significado de la frase. Los ejemplos entonces pasan a ser:
                            "visto por medio de un cine", "ir a casa por medio del autobús" y "comido almuerzo por medio de restaurante”.</p>



          <h1 id="sub-6">Cuando el lugar es el tema</h1>

                                <p>Hay veces en las que el lugar en que se produce la acción es también el tema de la frase. Cuando el tema es el lugar se pueden añadir las partículas （「は」 y 「も」）a las tres partículas que indican lugar （「に」、「へ」、「で」）. En el ejemplo siguiente veremos como el lugar puede convertirse en el tema. </p>
                                <p class="leccion">Ejemplo 1</p>
                                <p>ボブ：　<abbr title="がっこう - escuela">学校</abbr>に<abbr title="いく - ir">行った</abbr>？- ¿Fuiste a la escuela?</p>
                                <p>アリス：　<abbr title="いく - ir">行かなかった</abbr>。- No fui.</p>
                                <p>ボブ：　<abbr title="としょかん - biblioteca">図書館</abbr><b class="naranja">には</b>？ - ¿Y a la biblioteca?</p>
                                <p>アリス：　<abbr title="としょかん - biblioteca">図書館</abbr><b class="naranja">にも</b><abbr title="いく - ir">行かなかった</abbr>。 - Tampoco fui a la biblioteca.</p>

                                <p>En este ejemplo, Bob pone en juego un nuevo tema (librería), con lo que el lugar se convierte en el tema. La frase es, en realidad, una versión abreviada de 「図書館には行かなかった？」 que se puede deducir del contexto. </p>

                                <p class="leccion">Ejemplo 2</p>
                                <p>ボブ：<abbr title="どこ - dónde">どこ</abbr>で<abbr title="たべる - comer">食べる</abbr>？- ¿Comer donde?</p>
                                <p>アリス：<abbr title="イタリア - Itali">イタリア</abbr><abbr title="レストラン - restaurante">レストラン</abbr><b class="naranja">では</b><abbr title="どう - cómo/qué tal">どう</abbr>？ - ¿Que tal un restaurante italiano? </p>
                                <p>Bob pregunta, "¿Donde podemos comer?" y Alicia propone un restaurante italiano. Una frase del tipo, "Que te parece..." genera por lo general un nuevo tema, porque la persona está proponiendo algo nuevo. En este caso se propone el lugar (restaurante), de modo que éste pasa a ser el tema. </p>
                                <br><br>

          <h1 id="sub-7">Cuando el objeto directo es el tema</h1>

                                <p>La partícula de objeto directo se diferencia de las partículas relacionadas con el lugar en que, con ella, no se puede utilizar ninguna otra partícula al mismo tiempo. Por ejemplo, en la sección anterior tal vez hayas descubierto que puedes decir 「をは」 para presentar un objeto directo que sea también el tema, pero no es este el caso. Un tema puede ser un complemento directo sin que aparezca la partícula 「を」. De hecho, no es correcto poner la partícula 「を」. </p>
                                <p>（１）　<abbr title="にほんご - japones">日本語</abbr><b class="naranja">を</b><abbr title="ならう - aprender">習う</abbr>。 - Aprender japonés.</p>
                                <p>（２）　<abbr title="にほんご - japones">日本語</abbr><b class="naranja">は</b><abbr title="ならう - aprender">習う</abbr>。 - Con respecto al japonés, lo aprenderá. </p>

