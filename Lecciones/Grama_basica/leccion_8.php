          <h1 id="sub-1">Verbos Transitivos e Intransitivos</h1>

                                            <p>Algunos verbos japoneses presentan dos modalidades a las que se les denomina verbos transitivos e intransitivos. La diferencia entre ambos verbos es que uno de ellos es una acción ejecutada por un agente activo, mientras que el otro es una acción que se produce sin agente directo. En español a veces se pueden expresar ambos tipos de acción mediante el mismo verbo: “La pelota se ha soltado” vs. “solté la pelota” que en japonés sería: 「ボールが落ちた」 vs. 「ボールを落とした」. Hay veces en que, al poner las frases en español, hay que utilizar verbos diferentes. </p>

                                            <p>Como tanto el significado del verbo como el kanji son los mismos ¡podemos aprender dos verbos al precio de un solo kanji! Veamos una muestra de verbos transitivos e intransitivos. </p>

