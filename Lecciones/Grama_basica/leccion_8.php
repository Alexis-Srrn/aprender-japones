          <h1 id="sub-2">¡Cuidado con las partículas!</h1>

          <p>Lo más importante de esta lección es aprender a utilizar la partícula correcta con el verbo correcto. Como, desgraciadamente, el WWWJDIC no hace distinción entre los dos tipos de verbo, cuando nos encontremos con verbos nuevos nos va a resultar difícil saber cual es cual, o si dicho verbo presenta las formas transitivo/intransitivo. Lo único que puedo aconsejar es fijarse bien en las frases de ejemplo del [EX] link. Otra opción es conseguir una lista completa de verbos transitivos/intransitivos de alguna parte ¡y memorizarlos!</p>

          <p>（１）　<abbr title="わたし - yo">私</abbr>が<abbr title="でんき - luces/electricidad">電気</abbr><b class="naranja">を</b><abbr title="つける - encender">付けた</abbr>。- Yo soy el que encendió las luces.</p>
          <p>（２）　<abbr title="でんき - luces/electricidad">電気</abbr><b class="naranja">が</b><abbr title="つく - encenderse">付いた</abbr>。- Se encendieron las luces.</p>
          <p>（３）　<abbr title="でんき - luces/electricidad">電気</abbr><b class="naranja">を</b><abbr title="けす - borrar">消す</abbr>。- Apaga las luces.</p>
          <p>（４）　<abbr title="でんき - luces/electricidad">電気</abbr><b class="naranja">が</b><abbr title="きえる - desaparecer">消える</abbr>。- Las luces se apagan. </p>


          <p>Lo que hay que recordar principalmente es que los verbos intransitivos no pueden tener objeto directo. Las frases que vienen a continuación son gramaticalmente incorrectas. </p>
          <p>（誤）　電気<b class="naranja">を</b>付いた。- （「を」 habría que sustituirlo por 「が」 o 「は」）</p>
          <p>（誤）　電気<b class="naranja">を</b>消える。- （「を」　habría que sustituirlo por 「が」 o 「は」）</p>
          <p>（誤）　どうして窓<b class="naranja">を</b>開いた？- （「を」habría que sustituirlo por 「が」 o 「は」）</p>
