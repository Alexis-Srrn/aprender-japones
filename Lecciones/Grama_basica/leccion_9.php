          <h1 id="sub-1">Tratando a los verbos y al estado-de-ser como adjetivos</h1>

                          <p>¿Te has dado cuenta de que muchas formas de los verbos y el estado-de-ser se conjugan de forma parecida a como lo hacen los adjetivos-i? Pues bien, esto se debe a que, de cierta manera, son adjetivos. Por ejemplo, considera la frase: "La persona que no comió se fue al banco”. El "no comió" describe a la persona, y en japonés, puedes modificar directamente el nombre 'persona' con la oración 'no comió' como si se tratase de un adjetivo regular. Sólo saber algo tan sencillo como esto nos va a permitir modificar un nombre con cualquier frase verbal. </p><br>

          <h1 id="sub-2">Utilizando oraciones subordinadas de estado-de-ser como adjetivos</h1>

                          <p>Las conjugaciones negativa, pasada y negativa pasada de los nombres pueden utilizarse,
                            como los adjetivos, para modificar directamente al nombre. Sin embargo, no se puede
                            hacer esto con el presente informal del estado-de-ser utilizando 「だ」.
                           Para solucionar esto, el idioma tiene partículas que estudiaremos en la <a href="leccion_10.php">sección siguiente.</a> </p>

                           <p class="leccion">No se puede utilizar「だ」para modificar directamente a un nombre con
                             otro nombre como se hace con 「だった」、「じゃない」 y 「じゃなかった」.</p>

                          <p>Pero sí se puede formar una cadena de nombres si no se pretende que se modifiquen los unos a los otros. Un ejemplo, la frase "Centro Educación Internacional" es solo una cadena de nombres sin modificaciones gramaticales entre ellos.</p>

                          <p>Ejemplos<p>
                          <p>  Aquí tienes algunos ejemplos en los que una oración nominal conjugada modifica directamente al nombre. La oración nominal aparece resaltada.</p>

                          <p>（１）<b class="naranja"><abbr title="がくせい - estudiante">学生</abbr>じゃない</b>人は、<abbr title="がっこ - escuela">学校</abbr>に<abbr title="いく - ir">行かない</abbr>。</p>
                          <p>- Persona, que no es estudiante, no va a la escuela.</p>

                          <p>（２）　<b class="naranja"><abbr title="こども - niño/niña">子供</abbr>だった</b>アリスが<abbr title="りっぱ - estupendo/confiable">立派</abbr>な<abbr title="おとな - adulto">大人</abbr>に<abbr title="なる - volverse/convertirse">なった</abbr>。</p>
                          <p>- La Alicia, que era un niño, se ha convertido en un adulto estupendo.</p>

                          <p>（３）　<b class="naranja"><abbr title="ともだち - amigo">友達</abbr>じゃなかった</b>アリスは、<abbr title="いい - bueno">いい</abbr><abbr title="ともだち - amigo">友達</abbr>に<abbr title="なる - volverse/convertirse">なった</abbr>。</p>
                          <p>- La Alicia, que no era un amigo, se convirtió en un buen amigo.</p><br>




          <h1 id="sub-3">Utilizando oraciones subordinadas verbales como adjetivos</h1>

                                <p>Las oraciones verbales se pueden utilizar también, como los adjetivos, para modificar al nombre. En los ejemplos que aparecen a continuación verás que esto nos va a permitir construir frases muy detalladas y complicadas. La oración verbal aparece resaltada. </p>

                                <p class="leccion"> Ejemplos</p>

                                <p>（１）　<b class="naranja"><abbr title="せんしゅ - semana pasada">先週</abbr>に<abbr title="えいが - película">映画</abbr>を<abbr title="みる - ver">見た</abbr></b>人は<abbr title="だれ - ¿quién?">誰</abbr>？</p>
                                <p>- ¿Quien es persona que miraba película semana pasada?</p>

                                <p>  （２）　ボブは、<b class="naranja"><abbr title="いつも - siempre">いつも</abbr><abbr title="べんきょう - estudio">勉強</abbr><abbr ="する - hacer">する</abbr></b>人だ。</p>
                                <p>  - Bob es una persona que siempre estudia.</p>

                                <p>  （３）　<b class="naranja"><abbr title="あかい - rojo">赤い</abbr>ズボンを<abbr title="かう - comprar">買う</abbr></b><abbr title="ともだち - amigo">友達</abbr>はボブだ。</p>
                                <p>  - Amigo que compró pantalones rojos es Bob.</p><br>



          <h1 id="sub-4">El orden de la frase en japonés</h1>

          <p>Una vez aprendido el concepto de oraciones subordinadas, y su función de pieza para formar frases, podemos abordar el tema del orden de la frase japonesa. Circula por ahí un mito referente al orden de la frase japonesa que, por desgracia, confunde a muchos principiantes en el estudio del japonés. Es así como funciona: </p>
          <p>Podríamos decir que, en español, la frase de estructura más simple está compuesta por los siguientes elementos, en éste orden concreto: [Sujeto] [Verbo] [Objeto]. Una frase no es gramaticalmente correcta si falta alguno de estos elementos, o si no aparecen en ese orden.</p>

          <p>Los estudiantes de japonés te dirán con frustración que el japonés funciona completamente al revés. Incluso puede que tu profesor de japonés te diga que el orden de la frase japonesa es [Sujeto] [Objeto] [Verbo]. Éste es un típico intento de que el japonés se ajuste a la lógica del español. Naturalmente sabemos (¿a que sí?) que el verdadero orden de la más elemental de las frases japonesas es: [Verbo]. Cualquier cosa que venga antes del verbo no tiene que ir en ningún orden particular, y no se necesita más que el verbo para que haya una frase completa. Además, el verbo tiene que estar siempre al final de la frase. </p>

          <p>A esto se debe que existan las partículas, son ellas las que determinan qué función gramatical cumple cada palabra, ya que ésta es independiente de su posición en la frase. De hecho, se pueden construir frases con la estructura [Objeto] [Sujeto] [Verbo] o con solo [Objeto] [Verbo]. Todas las frases que aparecen a continuación son completas y correctas, ya que el verbo esta al final de la frase.</p>


          <p class="leccion">Frases gramaticalmente completas y correctamente ordenadas</p>


          <p>（１）　<abbr title="わたし - yo">私</abbr>は<abbr title="こうえん - parque">公園</abbr>で<abbr title="おべんとう - almuerzo">お弁当</abbr>を<b class="naranja"><abbr title="たべる - comer">食べました</abbr>。</b></p>
          <p>（２）　<abbr title="こうえん - parque">公園</abbr>で<abbr title="わたし - yo">私</abbr>は<abbr title="おべんとう - almuerzo">お弁当</abbr>を<b class="naranja"><abbr title="たべる - comer">食べました</abbr>。</b></p>
          <p>（３）　<abbr title="おべんとう - almuerzo">お弁当</abbr>を<abbr title="わたし - yo">私</abbr>は<abbr title="こうえん - parque">公園</abbr>で<b class="naranja"><abbr title="たべる - comer">食べました</abbr>。</b></p>
          <p>（４）　<abbr title="おべんとう - almuerzo">お弁当</abbr>を<b class="naranja"><abbr title="たべる - comer">食べました</abbr>。</b></p>
          <p>（５）　<b class="naranja"><abbr title="たべる - comer">食べた</abbr>。</b> </p><br>

          <p>Así que no te agobies pensando en si tu frase está en el orden correcto o no. Solo tienes que recordar las reglas siguientes:</p>
          <p class="leccion">Orden de la frase japonesa</p>

          <ol>
            <li>Una frase completa necesita de un verbo principal que debe aparecer al final. Incluido el estado-de-ser implícito. </li>
            <p>例） 食べた</p>
            <p>例） 学生（だ）</p><br>
            <li>Los nombres pueden modificarse mediante frases completas (oraciones subordinadas) dando lugar a frases con oraciones subordinadas anidadas</li>
            <p>例） <b class="naranja">お弁当を食べた</b>学生が公園に行った。</p>
        </ol><br><br>



