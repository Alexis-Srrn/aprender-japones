          <h1>¿Qué son los Hiragana?</h1>
          <p>El alfabeto Hiragana es el alfabeto fonético japonés básico. Está compuesto
            por todos los sonidos del idioma japonés, en la forma de un silabario.
        </p>
          <p>
              A continuación, presentamos la tabla de Hiragana, donde podrás consultar la escritura de todos los sonidos.
          </p>

            <img src="img/kana/tabla_hiragana.webp" alt="">


            <p>Realmente el hiragana no es difícil de memorizar. Con un estudio constante (y un tip que te daremos al final),
              deberías haberlo aprendido en un lapso no mayor a dos semanas.
              Te prepararemos con algunas reglas básicas que deben ser respetadas en todo momento.</p>





            <h1>Notas básicas</h1>

            <p>1. Excepto en el caso de 「し」、「ち」、「つ」 y 「ん」、puedes saber cómo se pronuncia cada
              letra emparejando la consonante de la fila superior con la vocal. Por ejemplo, 「
              き」 sería / ki / y 「ゆ」 sería / yu /, y así todo.</p>

            <p>2. Como puedes ver, no todos los sonidos de una columna coinciden del modo que lo hace
            nuestro sistema de consonantes. Como se ve en la tabla, 「ち」 se pronuncia "chi" y 「つ」
            se pronuncia "tsu".</p>


             <p>3. Los sonidos / r / y / l / del japonés equivalen a nuestra “r” suave.</p>

            <p>4. Presta especial atención a la diferencia entre / tsu / y / su /.</p>

            <p>5. El carácter 「ん」 es un carácter especial, no va acompañado por ningún sonido de vocal y
              nunca aparece solo. No se pronuncia exactamente igual que nuestra “n”, que articulamos pegando
              la lengua contra el paladar, justo detrás de los dientes superiores, sino que se emite
              con la boca cerrada, pegando la parte posterior de la lengua a la parte posterior del paladar.
              Es parecida a la m, pero pegando la lengua al paladar.</p>


              <h1>El Pequeño 「つ」</h1>
              <p>Si se inserta un pequeño 「つ」 entre dos caracteres se traslada el sonido de la consonante de la
                 segunda sílaba al final de la primera. Por ejemplo, si aparece un pequeño 「つ」 entre 「び」 y 「く」
                  formando 「びっく」, el sonido consonante / k / se traslada al final del primer carácter, dando "bikku".
                Del mismo modo 「はっぱ」 sería "happa", 「ろっく」 sería "rokku", etc. </p>




              <h1>¿Qué es el Katakana?</h1>

              <p>El katakana es un silabario más que sigue casi todas las mismas reglas del hiragana. ¿La diferencia?
                 Es usado para escribir palabras completamente extranjeras. Por esta misma razón su uso es menos
                  frecuente y lo vuelve más difícil de memorizar incluso para estudiantes avanzados.</p>
                <p>A continuación te presentamos la respectiva tabla de sonidos. Nota como sus trazos,
                  a diferencia del hiragana que son curvos, son completamente rectos.
                  Esto debe respetarse para evitar posibles confusiones al momento de escribir.</p>

            <img src="img/kana/tabla_katakana.jpg" alt="">

            <h1>Los sonidos adicionales</h1>

                <p>Como podrás darte cuenta, el katakana cuenta con una tabla adicional de sonidos. Estos fueron creados
                  para todas esas palabras del extranjero cuyos sonidos no existían en su vocabulario cotidiano.</p>

                  <p>Debido a lo limitado del conjunto de sonidos representados por los hiragana, a lo largo de los años
                    se han ido diseñando algunas combinaciones nuevas para representar los sonidos
                    que no había originalmente en japonés.</p>

                  <p>Se ha establecido también la convención de utilizar las pequeñas rayas dobles
                    con los sonidos vocales para representar la consonante / v /, pero no se utiliza a menudo,
                    probablemente debido al hecho de que los japoneses todavía tienen dificultades
                    para pronunciar la / v /. Por ejemplo, aunque podríamos pensar que la palabra
                     "volumen" se pronunciaría con un sonido / v /, los japoneses han optado por lo fácil,
                     pronunciar "bolumen" （ボリューム）. Del mismo modo, vodka se escribe "wokka" （ウォッカ）
                     y no 「ヴォッカ」</p>

                  <p>Se puede escribir "violín" de dos maneras: 「バイオリン」 o「ヴァイオリン」.
                    Pero en realidad no importa porque, en cualquier caso, casi todos los japoneses
                    lo van a pronunciar con un sonido / b /.</p>


                    <h1>¿Cómo los estudio?</h1>

                    <p>Seguramente estarás pensando en lo tedioso que puede resultar el tratar de memorizarlos.
                      No obstante, hay una pauta sencilla para lograrlo. Nota como en ambas tablas,
                      los primeros cuarenta y seis caracteres puede ser llamados los «básicos», los fundamentales del silabario.</p>


                    <p>A partir de estos los demás no son más que combinaciones y variaciones de estos sonidos.
                      Por poner un ejemplo, observa como [りゃ] se pronuncia “rya”, es decir りゃ(あ), y no es más que la
                      combinación de [り] y [や].
                      Así es como poco a poco podrás formar los sonidos por pura lógica y costumbre.</p>


