            <h1>¿Por qué Kanji?</h1>

            <p>Algunas personas creen que el sistema de utilizar símbolos en lugar de un práctico alfabeto está pasado de moda y
              es extraordinariamente complicado. El hecho es que puede no haber sido una buena idea adaptar el chino al japonés,
               dado que la estructura de estos dos idiomas es fundamentalmente diferente.
               Pero no es el propósito de esta guía discutir una decisión que se tomó hace miles de años,
               sino explicar porque hay que estudiar kanji para aprender japonés. Y con esto lo único que pretendo decir es:
               “así es como son las cosas, así que sigamos adelante”.</p>


              <img src="img/kanji/kanji.jpg" alt="">

              <p>Indiscutiblemente este será el paso más difícil en tu introducción al idioma y recomendamos no posponerlo.
                Si de verdad quieres aprender un japonés real,
                 y leer cualquier material nativo, los kanjis serán siempre tus mejores amigos.</p>

                <p>Aquí nos despediremos por un tiempo. Tómalo con calma y, cuando hayas terminado el estudio de los kanji,
                  podremos seguir a la gramática, donde será momento de comenzar a entender cómo funciona su estructura.
                </p>


