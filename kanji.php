          <h1>¿Qué es Kanji?</h1>
          <p>En japonés, casi todos los nombres y la raíz de los adjetivos y de los verbos se escriben en caracteres Chinos
            llamados kanji. Los adverbios también se escriben muy a menudo en kanji. Esto significa que vas a necesitar aprender
            los caracteres chinos para poder leer casi todas las palabras del idioma. Sin embargo, no todas las palabras se escriben
            en kanji. Por ejemplo, aunque el verbo “hacer” tiene técnicamente un kanji asociado a él, siempre se escribe en hiragana.
            A medida que hagamos inmersión desarrollaremos criterio propio para decidir en que momentos se usan estos caracteres.
        </p>

        <p>Se recomienda utilizar los kanji desde el principio para ayudar al lector a leer japonés “real” cuanto antes.
           Con este fin, vamos a dejar de lado determinadas propiedades de los kanji y vamos a pasar a discutir algunas
           estrategias para aprenderlos rápida y eficientemente.
        </p>
        <p>Es recomendable la adquisión del libro Remember The Kanji de James W. Heisig, pues los kanji pueden
          ser memorizados de mejor manera por medio de mnemotecnias y combinado con un sistema de repetición espaciada.
          A continuación te dejaremos un video del profesor Rafy donde explica a fondo el método. Tu objetivo debería ser
          memorizar los 2200 kanji de uso común en un lapso aproximado a cuatro meses:
        </p>

